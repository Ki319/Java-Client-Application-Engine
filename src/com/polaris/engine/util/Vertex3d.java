package com.polaris.engine.util;

public class Vertex3d 
{
	
	private double x;
	private double y;
	private double z;
	
	public double getX() 
	{
		return x;
	}
	
	public Vertex3d setX(double x)
	{
		this.x = x;
		return this;
	}
	
	public double getY()
	{
		return y;
	}
	
	public Vertex3d setY(double y)
	{
		this.y = y;
		return this;
	}
	
	public double getZ()
	{
		return z;
	}
	
	public Vertex3d setZ(double z) 
	{
		this.z = z;
		return this;
	}	

}
